import { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
    const baseUrl = process.env.NEXT_PUBLIC_APP_URL || 'https://cookiecraftai.com';

    return {
        rules: {
            userAgent: '*',
            allow: '/',
            disallow: ['/emails/', '/api/'],
        },
        sitemap: `${baseUrl}/sitemap.xml`,
    };
}
